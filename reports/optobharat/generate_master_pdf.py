class ColorBlock(Flowable):
    def __init__(self, width, height, color):
        Flowable.__init__(self)
        self.width = width
        self.height = height
        self.color = color
    def draw(self):
        self.canv.setFillColor(self.color)
        self.canv.rect(0, 0, self.width, self.height, fill=1, stroke=0)

def score_bar(score, max_score=10, width=200, height=16):
    """Create a score bar table"""
    pct = score / max_score
    elements = []
    # Background bar
    t = Table([['']], colWidths=[width], rowHeights=[height])
    t.setStyle(TableStyle([
        ('BACKGROUND', (0,0), (-1,-1), HexColor("#e2e8f0")),
        ('ROUNDEDCORNERS', [3,3,3,3]),
        ('VALIGN', (0,0), (-1,-1), 'MIDDLE'),
    ]))
    elements.append(t)
    # Filled bar
    fill_w = width * pct
    t2 = Table([['']], colWidths=[fill_w], rowHeights=[height])
    t2.setStyle(TableStyle([
        ('BACKGROUND', (0,0), (-1,-1), score_color(score)),
        ('ROUNDEDCORNERS', [3,3,3,3]),
        ('VALIGN', (0,0), (-1,-1), 'MIDDLE'),
    ]))
    elements.append(t2)
    # Score text
    score_style = ParagraphStyle('score', fontName='Helvetica-Bold', fontSize=9, textColor=TEXT_DARK, alignment=TA_RIGHT)
    elements.append(Paragraph(f"<b>{score}/{max_score}</b>", score_style))
    return elements

